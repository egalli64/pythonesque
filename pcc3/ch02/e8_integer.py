print("2 + 3 =", 2 + 3)
print("8 - 3 =", 8 - 3)
print("2 * 3 =", 2 * 3)
# The floating point division
print("2 / 3 =", 2 / 3)
# The integer division
print("2 // 3 =", 2 // 3)

# power
print("2 ** 3 =", 2 ** 3)

# evaluation order, multiplicative operations first
print("2 + 3 * 4 =", 2 + 3 * 4)

# forcing evaluation order with parentheses
print("(2 + 3) * 4 =", (2 + 3) * 4)
